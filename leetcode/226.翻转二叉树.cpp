/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode(int x) : val(x), left(NULL), right(NULL) {}
 * };
 */
class Solution {
public:
    TreeNode* invertTree(TreeNode* root) {
        if(!root) return nullptr;
        rev(root);
        return root;
    }

    void rev(TreeNode * p)
    {
        if(!p)
            return;
        swap( p->left , p->right );
        rev(p->left);
        rev(p->right);
    }
};
