/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode(int x) : val(x), left(NULL), right(NULL) {}
 * };
 */
class Solution {
public:
    int kthSmallest(TreeNode* root, int k) {
        if(!root ) return -1;
        stack<TreeNode*> stk;
        
        while(root || !stk.empty())
        {
            //找到最左子节点（最小的）
            while( root )
            {
                stk.push(root);
                root = root->left;
            }

            root = stk.top();
            
            --k;
            if(k == 0) return root->val;
            
            //切换到右子树
            root = root->right;
            stk.pop();
        }

        return -1;
    }
};
